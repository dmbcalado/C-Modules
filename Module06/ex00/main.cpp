#include "Converter.hpp"

int main(int argc, char **argv)
{
	if (argc != 2)
	{
		std::cout << "Please pass 1 and only 1 argument to be converted." << std::endl;
		return (-1);
	}
	Converter conv(argv[1]);
	conv.convertArgToType();
	return (0);
}